// Write a program which accept string from user and display it in reverse order. 
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

void Reverse(char *str)
{   
    int iCount = 0;

    while(*str != '\0')
    {
        iCount++;
        str++;
    }
    for(;iCount >= 0;iCount--,str--)
    {
        printf("%c",*str);
    }
}

int main()
{
    char Arr[20];

    printf("Enter String: ");
    scanf("%[^'\n']s",Arr);
    
    Reverse(Arr);

    return 0;
}